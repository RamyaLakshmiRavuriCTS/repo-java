package com.assignment.rolesAndResponsibilities;

abstract class RolesAndResponsibilities {
    //creating all abstract methods that are going to be implemented in its sub class
    abstract String getName();
    abstract void setName(String name);
    abstract String getAddress();
    abstract void setAddress(String address);
    abstract String getRole();
    abstract void setRole(String role);
    abstract String getResponsibilities();
    abstract void setResponsibilities(String responsibilities);
}
